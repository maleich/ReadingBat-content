def times_5(x):
    return x * 5


def main():
    print(times_5('hello'))
    print(times_5('world'))
    print(times_5('apples'))
    print(times_5(''))
    print(times_5('a'))
    print(times_5('oof'))


if __name__ == '__main__':
    main()
